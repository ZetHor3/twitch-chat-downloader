def tr(lang: str, key: str, **kwargs) -> str:
    """Translate *key* to *lang*, optionally formatting with *kwargs.

    Falls back to English if the key or language is missing.
    """
    msg = LOCALES.get(lang, {}).get(key) or LOCALES["en"].get(key, key)
    if kwargs:
        return msg.format(**kwargs)
    return msg
